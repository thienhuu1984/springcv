package funix.huutt.springcv.view;

public class Statistics {

    protected int totalCandidate;
    protected int totalCompanies;
    protected int totalRecruitments;

    public Statistics() {
    }

    public int getTotalCandidate() {
        return totalCandidate;
    }

    public void setTotalCandidate(int totalCandidate) {
        this.totalCandidate = totalCandidate;
    }

    public int getTotalCompanies() {
        return totalCompanies;
    }

    public void setTotalCompanies(int totalCompanies) {
        this.totalCompanies = totalCompanies;
    }

    public int getTotalRecruitments() {
        return totalRecruitments;
    }

    public void setTotalRecruitments(int totalRecruitments) {
        this.totalRecruitments = totalRecruitments;
    }
}
